#Write a Python program to convert kilometers to miles.
kilometrs = float(input("Enter distance in kilometers: "))
#conversion_factor = 0.621371
miles = kilometrs * 0.621371
print(f"{kilometrs} kilometers is equal to {miles} miles.")

#write a python program to convert kilometers to meters
kilometrs = float(input("Enter distance in kilometers: "))
meters = kilometrs * 1000
print(f"{kilometrs} kilometers is equal to {meters} meters.")